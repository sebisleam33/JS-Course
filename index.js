const grade = 'A';

switch(grade) {
    case 'A':
        console.log('Great!');
        break;
    case 'B':
        console.log('Good');
        break;
    case 'C':
        console.log('Average');
        break;
    case 'D':
        console.log('Bad');
        break;
    case 'F':
        console.log('Fail');
        break;
    default:
        console.log('Invalid grade');
}